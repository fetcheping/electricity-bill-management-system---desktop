    public void searchInTable(){
         if (txtsearch.getText().isEmpty()) {
            loadDataClient();
        }else {
            DefaultTableModel dtm = new DefaultTableModel();

            dtm.addColumn("FirstName");
            dtm.addColumn("LastName");
            dtm.addColumn("BirthDate");
            dtm.addColumn("Phone");
            dtm.addColumn("Gender");
            dtm.addColumn("Email");
            ClientDAO cltD = new ClientDAO();
            for (Client clt: cltD.find(txtsearch.getText().trim().toLowerCase())) {
                dtm.addRow(new Object[]{
                    clt.getFirstname(),
                    clt.getLastname(),
                    clt.getBirthdate(),
                    clt.getPhonenumber(),
                    clt.getGender(),
                    clt.getEmail()
                });
                this.jtableclient.setModel(dtm);
            
        }
    }
    }
    public void findContract(){
        if (txtsearchctr.getText().isEmpty()) {
            loadDataContract();
        }else {
            DefaultTableModel dtm = new DefaultTableModel();
        TableColumnModel tcm = jtablecontract.getColumnModel();
        dtm.addColumn("Contract Number");
        dtm.addColumn("Client");
        dtm.addColumn("Date Of Contract");
        dtm.addColumn("Meter Number");
        ContractDAO ctrdao = new ContractDAO();
        // get the value of the first column
        //jTableUser.getValueAt(jTableUser.getSelectedRow(),0);
        List list = ctrdao.find(txtsearchctr.getText().trim().toLowerCase());
        for (Iterator iterator1 = list.iterator(); iterator1.hasNext();) {
           Client c = (Client)iterator1.next();
            Set ctr =  c.getContracts();
            for (Iterator iterator2 = ctr.iterator(); iterator2.hasNext();) {
               Contract ctrt = (Contract)iterator2.next();
               dtm.addRow(new Object[]{
               ctrt.getContractNumber(),
               c.getFirstname(),
               ctrt.getContractDate(),
               ctrt.getMeterNumber()
            });
            this.jtablecontract.setModel(dtm);
            }
       
        }
    } 
    }
